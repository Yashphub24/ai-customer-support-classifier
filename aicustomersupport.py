# THE "OBVIOUS" KEYWORD APPROACH — adapted to this dataset's 5 categories
# ===============================================================
def keyword_classify(text):
    """The 'obvious' approach that doesn't work well."""
    text = str(text).lower()

    if any(word in text for word in ['bill', 'charge', 'invoice', 'payment', 'price']):
        return 'Billing inquiry'
    elif any(word in text for word in ['crash', 'error', 'bug', 'slow', 'broken', 'fix', 'not working']):
        return 'Technical issue'
    elif any(word in text for word in ['cancel', 'terminate', 'close my account', 'stop my subscription']):
        return 'Cancellation request'
    elif any(word in text for word in ['refund', 'money back', 'reimburse']):
        return 'Refund request'
    elif any(word in text for word in ['feature', 'how to', 'upgrade', 'plan', 'compatible']):
        return 'Product inquiry'
    else:
        return 'Unknown'

tickets['keyword_prediction'] = tickets['ticket_description'].apply(keyword_classify)

correct = (tickets['keyword_prediction'] == tickets['ticket_type']).sum()

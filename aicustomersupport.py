# BUILD AN INTERACTIVE APP - Right here in the notebook!
# ===============================================================
import gradio as gr

def classify_ticket(ticket_text):
    """Classify a customer support ticket using our trained model."""
    if not ticket_text or not ticket_text.strip():
        return {"Please enter a ticket description": 1.0}

    # Generate embedding
    embedding = model.encode([ticket_text])

    # Predict with probabilities
    probabilities = classifier.predict_proba(embedding)[0]

    # Return as a dictionary for Gradio Label component
    results = {
        cat: float(prob)
        for cat, prob in zip(classifier.classes_, probabilities)
    }
    return results

# Create the Gradio interface
demo = gr.Interface(
    fn=classify_ticket,
    inputs=gr.Textbox(
        label="Paste a customer support ticket",
        placeholder="e.g., 'My account was charged twice this month and I need a refund'",
        lines=3,
    ),
    outputs=gr.Label(
        label="Predicted Category",
        num_top_classes=5,
    ),
    title="AI Customer Support Ticket Classifier",
    description=(
        "Paste any support ticket below and the AI will instantly categorize it. "
        "This uses the same embedding + classification pipeline we just built."
    ),
    examples=[
        ["My internet connection keeps dropping every few hours and I've restarted the router multiple times"],
        ["I was charged $49.99 but my plan is only $29.99 per month - please explain the extra charge"],
        ["How do I export my project data to CSV format?"],
        ["I want to cancel my subscription - please process this immediately"],
        ["Please process a refund, the service was down for a full week"],
    ],
    theme=gr.themes.Soft()
)

print("Launching interactive app...")
print("Click the examples below or type your own ticket!")
demo.launch(share=True)